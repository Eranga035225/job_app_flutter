class JobModel{

  String title;
  String type;
  String salary;
  String companyName;
  String companyLogo;
  String city;
  String time;


  JobModel({
    required this.title,
    this.type = 'Full time',
    required this.salary,
    required this.companyName,
    required this.companyLogo,
    required this.city,
    required this.time,
  });


}

  








